export function PixelIcon({
  name,
  className,
  style,
  size = 16,
}: PixelIconProps) {
  const grid = iconGrids[name];
  if (!grid) {
    return null;
  }

  const primaryPath: string[] = [];
  const accentPath: string[] = [];

  for (let y = 0; y < 16; y++) {
    const row = grid[y] || "";
    for (let x = 0; x < 16; x++) {
      const char = row[x];
      if (char === "#") {
        primaryPath.push(`M${x},${y}h1v1h-1z`);
      } else if (char === "+") {
        accentPath.push(`M${x},${y}h1v1h-1z`);
      }
    }
  }

  return (
    <svg
      viewBox="0 0 16 16"
      width={size}
      height={size}
      className={className}
      style={{
        shapeRendering: "crispEdges",
        display: "inline-block",
        verticalAlign: "middle",
        flexShrink: 0,
        ...style,
      }}
    >
      {primaryPath.length > 0 && (
        <path d={primaryPath.join("")} fill="currentColor" />
      )}
      {accentPath.length > 0 && (
        <path d={accentPath.join("")} fill="currentColor" opacity="0.4" />
      )}
    </svg>
  );
}
